public class try2 {
    public static void main(String[] args) {
        int n = 5;
        switch (n){
            case 1:
                System.out.println("ONE");
                break;
            case 2:
                System.out.println("TWO");
                break;
            case 3:
                System.out.println("Three");
                break;
            case 4:
                System.out.println("Four");
                break;
            case 5:
                System.out.println("Five");
                break;

                default:
                System.out.println("Please Enter the numeber between 1 to 5");
        }
    }
}
